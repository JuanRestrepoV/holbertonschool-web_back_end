#!/usr/bin/env python3
"""
function concat that takes
a string str1 and a string
str2 as arguments and returns
a concatenated string
"""


def concat(str1: str, str2: str) -> str:
    """Args:
        str1 (str): string
        str2 (str): string

    Returns:
        str: a concat string
    """
    return str1 + str2
